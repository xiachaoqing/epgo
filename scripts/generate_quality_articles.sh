# 插入文章的函数
insert_article() {
    local title="$1"
    local keywords="$2"
    local description="$3"
    local class2="$4"
    local content=$(generate_content "$title" "$keywords")

    # 转义单引号
    content=$(echo "$content" | sed "s/'/''/g")

    mysql -h"$DB_HOST" -u"$DB_USER" -p"$DB_PASS" "$DB_NAME" << EOF 2>/dev/null
INSERT INTO hh_news
(title, keywords, description, content, class1, class2, wap_ok, img_ok, lang, addtime, access, publisher, hits)
VALUES
('$title', '$keywords', '$description', '$content', 128, $class2, 1, 0, 'cn', NOW(), 0, '$PUBLISHER', 0);
EOF

    echo "✓ 已插入: ${title:0:50}... (class2=$class2)"
}

echo "【删除旧数据】"
# 删除之前class2=0的文章（保留其他分类）
